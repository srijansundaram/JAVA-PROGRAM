public class Basics_Literals {
    public static void main(String[] args) {
        //  Value stored in any variable is literal
        int age = 34; // 34 is a integer literal
        char ch = 'a';
        float f1 = 5.3f;
        double d1 = 4.66d; // writing d is not mandatory
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
    }
}